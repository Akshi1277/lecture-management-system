import mongoose from 'mongoose';

const roomBlockSchema = mongoose.Schema({
    room: {
        type: String,
        required: true,
    },
    startTime: {
        type: Date,
        required: true,
    },
    endTime: {
        type: Date,
        required: true,
    },
    reason: {
        type: String,
        required: true,
    },
    createdBy: {
        type: mongoose.Schema.Types.ObjectId,
        ref: 'User',
        required: true,
    }
}, {
    timestamps: true
});

const RoomBlock = mongoose.model('RoomBlock', roomBlockSchema);
export default RoomBlock;
